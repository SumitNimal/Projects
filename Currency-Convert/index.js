//Getting option For Currenc
let selectedTo  = 'INR',selectedFrom = 'USD';
const selectCurrFrom = document.querySelector('.select-currency-from');
const selectCurrTo = document.querySelector('.select-currency-to');
selectCurrFrom.value  = selectedFrom;
selectCurrTo.value  = selectedTo;

function trackCurrencyChange(element,value,flag){
    element.addEventListener('change',(event)=>{
        value = event.target.value;
        if(flag === 'from') selectedFrom = value;
        else selectedTo=value;
        //change flag
        document.querySelector(`.flag-${flag}`).src = `https://flagsapi.com/${countryList[value]}/flat/64.png`;
        
    });    
};

trackCurrencyChange(selectCurrFrom,selectedFrom,'from');
trackCurrencyChange(selectCurrTo,selectedTo,'to');

async function changeCurrency(selectedFrom,selectedTo){
    let URL = `${url}${selectedFrom.toLowerCase()}.json`;
    let p = await fetch(URL);
    let data = await p.json();
    let exr = data[selectedFrom.toLowerCase()][selectedTo.toLowerCase()];
    let inputvalue = document.querySelector('.amount').value;
    if(inputvalue>=0){
        document.querySelector('.result').innerHTML = `${inputvalue} ${selectedFrom} = ${(inputvalue*exr).toFixed(2)} ${selectedTo}`;
    }
}

document.querySelector('.btn').addEventListener('click',() => {
    changeCurrency(selectedFrom,selectedTo);
});